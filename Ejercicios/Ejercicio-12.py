# Operaciones aritmeticas

print("Deme un valor 1")

a = int(input())

print("Deme un valor 2")

b = int(input())

# Ejemplo suma

suma = a + b

print(suma)

# Ejemplo resta

resta = a - b

print(resta)

# Ejemplo multiplicacion

multiplicacion = a * b

print(multiplicacion)

# Ejemplo division

division = a / b

print(division)

# Ejemplo modulo

modulo = a % b

print(modulo)

# Ejemplo exponente

exponente = a ** b

print(exponente)
